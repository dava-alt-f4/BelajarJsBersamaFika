// String var
let name = "Budiman";
console.log("My name is " + name);

// Number var
let age = 20;
console.log("My age is " + age);

let height = 170.5;

console.log("My height is " + height);

// Boolean var
let isOnline = true;
let isLogin = false;

console.log("Am I online? " + isOnline);
console.log("Am I logged in? " + isLogin);

// Undefined var
let address;
console.log("My address is " + address);

// Null var
let phone = null;
console.log("My phone number is " + phone);

// symbol var
let id = Symbol("id");
console.log("My id is " + id.toString());

// bigint var
let bigNumber = 9007199254741991n;
console.log("My big number is " + bigNumber);
